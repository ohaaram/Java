package sub26;

public class Student01 {
	private String name;
	private String sex;
	private int score;
	
	
	public Student01(String name, String sex, int score) {
		this.name = name;
		this.sex = sex;
		this.score = score;
	}


	public String getName() {
		return name;
	}


	public String getSex() {
		return sex;
	}


	public int getScore() {
		return score;
	}



	

}
